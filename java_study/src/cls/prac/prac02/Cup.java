package cls.prac.prac02;

public class Cup {
	
	String color;
	String jj;
	int ml;
	int cnt;
	
	public Cup(){
		color = "black";
		jj = "glass";
		ml = 0;
		cnt = 0;
	}
	
	public Cup(String color, String jj, int ml, int cnt ){
		this.color = color;
		this.jj = jj;
		this.ml = ml;
		this.cnt = cnt;
	}

}
